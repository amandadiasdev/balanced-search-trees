"""Generate the two compact figures selected for the empirical report."""

import csv
import math
from collections import defaultdict
from collections.abc import Sequence
from pathlib import Path
from statistics import median

import matplotlib

matplotlib.use("Agg")

import matplotlib.pyplot as plt

from helper_functions import PortugueseArgumentParser

REQUIRED_FIELDS = {
    "status",
    "trace_id",
    "implementation",
    "operations_requested",
    "theta",
    "insert_order",
    "repetition",
    "insert_mean_ns",
    "insert_p99_ns",
    "delete_mean_ns",
    "wall_time_ns",
}
IMPLEMENTATIONS = ("avl", "sorted-list")
ORDERS = ("sorted", "shuffle")
ORDER_LABELS = {"sorted": "Ordenada", "shuffle": "Embaralhada"}
ORDER_COLORS = {"sorted": "#009E73", "shuffle": "#CC79A7"}
IMPLEMENTATION_LABELS = {"avl": "AVL", "sorted-list": "Lista ordenada"}
IMPLEMENTATION_COLORS = {"avl": "#0072B2", "sorted-list": "#D55E00"}


def load_successful_rows(csv_path: Path) -> list[dict[str, str]]:
    """Load successful benchmark rows and validate the plotting schema.

    Args:
        csv_path: Wide CSV generated by ``run_experiments.py``.

    Returns:
        Successful rows ready for aggregation.

    Raises:
        ValueError: If required columns or successful rows are missing.
    """
    with csv_path.open(newline="", encoding="utf-8") as results_file:
        reader = csv.DictReader(results_file)
        missing_fields = REQUIRED_FIELDS - set(reader.fieldnames or ())
        if missing_fields:
            missing = ", ".join(sorted(missing_fields))
            raise ValueError(f"o CSV não possui as colunas obrigatórias: {missing}")
        rows = [row for row in reader if row["status"] == "sucesso"]
    if not rows:
        raise ValueError("o CSV não possui execuções com status=sucesso")
    return rows


def paired_ratios(rows: list[dict[str, str]], metric: str, selected_theta: float) -> dict[str, dict[int, list[float]]]:
    """Calculate paired list-to-AVL ratios before aggregating repetitions.

    Args:
        rows: Successful benchmark rows.
        metric: Numeric CSV field compared between implementations.
        selected_theta: Theta value held constant in the scale study.

    Returns:
        Ratios grouped by insertion order and requested operations.

    Raises:
        ValueError: If a trace and repetition do not contain both implementations.
    """
    pairs: dict[tuple[str, int], dict[str, dict[str, str]]] = defaultdict(dict)
    for row in rows:
        if math.isclose(float(row["theta"]), selected_theta):
            pair_key = (row["trace_id"], int(row["repetition"]))
            pairs[pair_key][row["implementation"]] = row

    ratios: dict[str, dict[int, list[float]]] = {order: defaultdict(list) for order in ORDERS}
    for pair_key, implementations in pairs.items():
        if set(implementations) != set(IMPLEMENTATIONS):
            raise ValueError(f"o par {pair_key} não contém AVL e lista ordenada")
        avl_row = implementations["avl"]
        list_row = implementations["sorted-list"]
        order = avl_row["insert_order"]
        operations = int(avl_row["operations_requested"])
        ratios[order][operations].append(float(list_row[metric]) / float(avl_row[metric]))
    if not pairs:
        raise ValueError(f"o CSV não possui pares completos para theta={selected_theta}")
    return ratios


def compact_operations(value: int) -> str:
    """Format an operation count for a compact PT-BR axis label.

    Args:
        value: Requested operation count.

    Returns:
        Compact label using ``mil`` when appropriate.
    """
    if value >= 1_000_000 and value % 1_000_000 == 0:
        return f"{value // 1_000_000} mi"
    if value >= 1_000 and value % 1_000 == 0:
        return f"{value // 1_000} mil"
    return f"{value:,}".replace(",", ".")


def configure_style() -> None:
    """Configure a restrained, report-ready Matplotlib style."""
    plt.rcParams.update(
        {
            "font.size": 10,
            "axes.titlesize": 12,
            "axes.labelsize": 10,
            "legend.fontsize": 9,
            "figure.titlesize": 14,
            "axes.spines.top": False,
            "axes.spines.right": False,
            "svg.fonttype": "none",
        }
    )


def plot_speedup(rows: list[dict[str, str]], output_path: Path, selected_theta: float = 0.99) -> Path:
    """Plot implementation speedup across scales and insertion orders.

    Args:
        rows: Successful benchmark rows.
        output_path: Destination SVG file.
        selected_theta: Theta held constant in the scale comparison.

    Returns:
        Path of the generated SVG.
    """
    configure_style()
    metrics = (
        ("insert_mean_ns", "Inserção"),
        ("wall_time_ns", "Workload completo"),
    )
    figure, axes = plt.subplots(1, 2, figsize=(11, 4.4), sharey=True, layout="constrained")
    largest_operations = max(int(row["operations_requested"]) for row in rows)

    for axis, (metric, title) in zip(axes, metrics, strict=True):
        ratios = paired_ratios(rows, metric, selected_theta)
        all_ratios: list[float] = []
        for order in ORDERS:
            operations = sorted(ratios[order])
            medians = [median(ratios[order][value]) for value in operations]
            all_ratios.extend(ratio for value in operations for ratio in ratios[order][value])
            axis.plot(
                operations,
                medians,
                color=ORDER_COLORS[order],
                marker="o",
                linewidth=2.2,
                label=ORDER_LABELS[order],
            )
            for value in operations:
                axis.scatter(
                    [value] * len(ratios[order][value]),
                    ratios[order][value],
                    color=ORDER_COLORS[order],
                    alpha=0.28,
                    s=22,
                    edgecolors="none",
                )
        axis.axhline(1.0, color="#555555", linestyle="--", linewidth=1.2)
        axis.text(
            0.97,
            0.51,
            "AVL ↑\nLista ↓",
            transform=axis.transAxes,
            color="#333333",
            ha="right",
            va="bottom",
        )
        axis.set_xscale("log")
        scale_values = sorted({int(row["operations_requested"]) for row in rows})
        axis.set_xticks(scale_values, [compact_operations(value) for value in scale_values])
        axis.set_ylim(0, max(2.05, max(all_ratios) * 1.12))
        axis.set_title(title)
        axis.set_xlabel("Operações solicitadas")
        axis.grid(axis="y", color="#D9D9D9", linewidth=0.7)

    axes[0].set_ylabel("Razão de latência: lista / AVL (×)")
    handles, labels = axes[0].get_legend_handles_labels()
    figure.legend(handles, labels, loc="lower center", ncols=2, bbox_to_anchor=(0.5, -0.04), frameon=False)
    theta_label = f"{selected_theta:g}".replace(".", ",")
    figure.suptitle(f"Efeito da escala e da ordem — θ={theta_label}")

    p99_values: dict[str, list[float]] = defaultdict(list)
    for row in rows:
        if (
            int(row["operations_requested"]) == largest_operations
            and row["insert_order"] == "shuffle"
            and math.isclose(float(row["theta"]), selected_theta)
        ):
            p99_values[row["implementation"]].append(float(row["insert_p99_ns"]) / 1_000)
    if set(p99_values) == set(IMPLEMENTATIONS):
        avl_p99 = f"{median(p99_values['avl']):.1f}".replace(".", ",")
        list_p99 = f"{median(p99_values['sorted-list']):.1f}".replace(".", ",")
        axes[0].text(
            0.04,
            0.94,
            f"p99 na maior escala\nAVL {avl_p99} µs · Lista {list_p99} µs",
            transform=axes[0].transAxes,
            ha="left",
            va="top",
            bbox={"boxstyle": "round,pad=0.35", "facecolor": "white", "edgecolor": "#BBBBBB"},
        )

    figure.savefig(output_path, format="svg", bbox_inches="tight", metadata={"Creator": "Matplotlib"})
    plt.close(figure)
    return output_path


def plot_theta_impact(rows: list[dict[str, str]], output_path: Path) -> Path:
    """Plot theta sensitivity at the largest scale with the complete sweep.

    Args:
        rows: Successful benchmark rows.
        output_path: Destination SVG file.

    Returns:
        Path of the generated SVG.

    Raises:
        ValueError: If no scale contains every theta, order, and implementation.
    """
    configure_style()
    required_theta_values = {float(row["theta"]) for row in rows}
    operations_values = sorted({int(row["operations_requested"]) for row in rows}, reverse=True)
    largest_operations = next(
        (
            operations
            for operations in operations_values
            if all(
                {
                    float(row["theta"])
                    for row in rows
                    if int(row["operations_requested"]) == operations
                    and row["insert_order"] == order
                    and row["implementation"] == implementation
                }
                == required_theta_values
                for order in ORDERS
                for implementation in IMPLEMENTATIONS
            )
        ),
        None,
    )
    if largest_operations is None:
        raise ValueError("nenhuma escala contém a variação completa de theta")
    largest_rows = [row for row in rows if int(row["operations_requested"]) == largest_operations]
    figure, axes = plt.subplots(1, 2, figsize=(11, 4.4), sharey=True, layout="constrained")

    for axis, order in zip(axes, ORDERS, strict=True):
        order_rows = [row for row in largest_rows if row["insert_order"] == order]
        for implementation_index, implementation in enumerate(IMPLEMENTATIONS):
            implementation_rows = [row for row in order_rows if row["implementation"] == implementation]
            values_by_theta: dict[float, list[float]] = defaultdict(list)
            for row in implementation_rows:
                values_by_theta[float(row["theta"])].append(float(row["delete_mean_ns"]) / 1_000)
            if not values_by_theta:
                raise ValueError(
                    "a maior escala com variação completa não possui "
                    f"{IMPLEMENTATION_LABELS[implementation]} na ordem {ORDER_LABELS[order]}"
                )
            theta_values = sorted(values_by_theta)
            median_values = [median(values_by_theta[theta]) for theta in theta_values]
            axis.plot(
                theta_values,
                median_values,
                color=IMPLEMENTATION_COLORS[implementation],
                marker="o",
                linewidth=2.2,
                label=IMPLEMENTATION_LABELS[implementation],
            )
            offset = -0.012 if implementation_index == 0 else 0.012
            for theta in theta_values:
                axis.scatter(
                    [theta + offset] * len(values_by_theta[theta]),
                    values_by_theta[theta],
                    color=IMPLEMENTATION_COLORS[implementation],
                    alpha=0.28,
                    s=22,
                    edgecolors="none",
                )
        axis.set_title(f"Inserções {ORDER_LABELS[order].lower()}s")
        axis.set_xlabel("θ")
        theta_ticks = sorted({float(row["theta"]) for row in order_rows})
        axis.set_xticks(theta_ticks, [f"{theta:.2f}".replace(".", ",") for theta in theta_ticks])
        axis.grid(axis="y", color="#D9D9D9", linewidth=0.7)

    axes[0].set_ylabel("Latência média de remoção (µs)")
    sorted_list_rows = [
        row for row in largest_rows if row["insert_order"] == "sorted" and row["implementation"] == "sorted-list"
    ]
    theta_min = min(float(row["theta"]) for row in sorted_list_rows)
    theta_max = max(float(row["theta"]) for row in sorted_list_rows)
    initial = median(float(row["delete_mean_ns"]) for row in sorted_list_rows if float(row["theta"]) == theta_min)
    final = median(float(row["delete_mean_ns"]) for row in sorted_list_rows if float(row["theta"]) == theta_max)
    reduction = 100 * (1 - final / initial)
    axes[0].text(
        0.04,
        0.06,
        f"Lista ordenada: redução de {reduction:.0f}%",
        transform=axes[0].transAxes,
        ha="left",
        va="bottom",
        bbox={"boxstyle": "round,pad=0.35", "facecolor": "white", "edgecolor": "#BBBBBB"},
    )

    handles, labels = axes[0].get_legend_handles_labels()
    figure.legend(handles, labels, loc="lower center", ncols=2, bbox_to_anchor=(0.5, -0.04), frameon=False)
    figure.suptitle(f"Impacto de θ na remoção — {compact_operations(largest_operations)} operações")
    figure.savefig(output_path, format="svg", bbox_inches="tight", metadata={"Creator": "Matplotlib"})
    plt.close(figure)
    return output_path


def main(argv: Sequence[str] | None = None) -> int:
    """Load one result CSV and generate the report figures.

    Args:
        argv: Optional command-line arguments for tests and embedding.

    Returns:
        Process exit code.
    """
    parser = PortugueseArgumentParser(description=__doc__)
    parser.add_argument("csv", metavar="CSV", type=Path, help="arquivo de resultados do estudo")
    parser.add_argument(
        "--saida",
        metavar="DIRETÓRIO",
        type=Path,
        default=Path("results/graficos"),
        help="diretório dos SVGs gerados (padrão: results/graficos)",
    )
    args = parser.parse_args(argv)
    try:
        rows = load_successful_rows(args.csv)
        args.saida.mkdir(parents=True, exist_ok=True)
        outputs = (
            plot_speedup(rows, args.saida / "speedup-escala-ordem.svg"),
            plot_theta_impact(rows, args.saida / "impacto-theta-remocao.svg"),
        )
    except (OSError, ValueError, ZeroDivisionError) as error:
        parser.error(str(error))
    for output in outputs:
        print(f"[sucesso] gráfico {output} gerado")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
